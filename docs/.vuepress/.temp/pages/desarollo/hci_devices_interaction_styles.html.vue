<h2 id="_4-interaction-styles" tabindex="-1"><a class="header-anchor" href="#_4-interaction-styles"><span>4. Interaction Styles</span></a></h2>
<ul>
<li><strong>Command-Line Interface</strong>: Text-based commands.</li>
<li><strong>Menu &amp; Navigation</strong>: Hierarchical menus for user actions.</li>
<li><strong>Natural Language Interaction</strong>: Voice, gestures, etc.</li>
<li><strong>Direct Manipulation</strong>: Dragging, dropping, and manipulating on-screen objects.</li>
<li><strong>Assisted Interaction</strong>: Agents or wizards guiding the user.</li>
<li><strong>Common in Web</strong>: Menu &amp; navigation-based style.</li>
</ul>
