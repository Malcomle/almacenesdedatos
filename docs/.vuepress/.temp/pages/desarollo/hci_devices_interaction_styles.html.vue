<h2 id="_3-devices" tabindex="-1"><a class="header-anchor" href="#_3-devices"><span>3. Devices</span></a></h2>
<h3 id="physical-devices" tabindex="-1"><a class="header-anchor" href="#physical-devices"><span>Physical Devices</span></a></h3>
<ul>
<li><strong>Keyboard</strong>, <strong>Screen</strong>, <strong>Pointing Devices</strong>, <strong>Voice Devices</strong>, <strong>Eye-Tracking Devices</strong>.</li>
</ul>
<h3 id="logical-devices" tabindex="-1"><a class="header-anchor" href="#logical-devices"><span>Logical Devices</span></a></h3>
<ul>
<li>Abstract elements that provide specific data types (Pointer, Text Input, Choice Input, etc.).</li>
<li><strong>Basic Interaction Tasks (BIT)</strong>: Locator, Quantity, Text, Choice, Drag.</li>
</ul>
<h3 id="matching-devices" tabindex="-1"><a class="header-anchor" href="#matching-devices"><span>Matching Devices</span></a></h3>
