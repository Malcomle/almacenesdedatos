<p>Activity design defines how users accomplish tasks within a system, focusing on user-centered and methodological approaches.</p>
<hr>
<h2 id="_1-motivation-for-ui-design" tabindex="-1"><a class="header-anchor" href="#_1-motivation-for-ui-design"><span>1. Motivation for UI Design</span></a></h2>
<ul>
<li>After gathering requirements, determine <strong>how</strong> to implement functionalities.</li>
<li>Example: Web application for searching establishments:
<ul>
<li>Single page or multiple pages?</li>
<li>How does the user access and view results?</li>
</ul>
</li>
</ul>
<hr>
<h2 id="_2-user-centered-design" tabindex="-1"><a class="header-anchor" href="#_2-user-centered-design"><span>2. User-Centered Design</span></a></h2>
<ul>
<li><strong>Activity Design</strong>: Analyzing the tasks and interactions.</li>
<li><strong>Information Design</strong>: Deciding how information is presented.</li>
</ul>
<hr>
<h2 id="_3-approaches-to-ui-design" tabindex="-1"><a class="header-anchor" href="#_3-approaches-to-ui-design"><span>3. Approaches to UI Design</span></a></h2>
<ul>
<li><strong>Empirical Approach</strong>: Designer expertise, guidelines, best practices.</li>
<li><strong>Methodological Approach</strong>: Theoretical foundations, structured steps (focus of this unit).</li>
</ul>
<hr>
<h2 id="_4-methodological-approach-steps" tabindex="-1"><a class="header-anchor" href="#_4-methodological-approach-steps"><span>4. Methodological Approach Steps</span></a></h2>
<ol>
<li><strong>Analyze Human Factors</strong>: Understand how users perceive and act.</li>
<li><strong>User-Centered Design</strong>: Keep user goals in focus.</li>
<li><strong>Task Analysis</strong>: Represent tasks (HTA, CTT, UML).</li>
<li><strong>Dialogue Modeling</strong>: Represent system-user communication.</li>
<li><strong>Information Design Strategies</strong>: Plan how data is shown and organized.</li>
</ol>
<hr>
<h2 id="_5-mental-model-vs-conceptual-model" tabindex="-1"><a class="header-anchor" href="#_5-mental-model-vs-conceptual-model"><span>5. Mental Model vs. Conceptual Model</span></a></h2>
<ul>
<li><strong>Mental Model</strong>: User’s internal, often incomplete, representation of how a system works.</li>
<li><strong>Conceptual Model</strong>: Developer’s formal design, ideally aligned with the user’s mental model.</li>
</ul>
<hr>
<h2 id="_6-activities-in-activity-design" tabindex="-1"><a class="header-anchor" href="#_6-activities-in-activity-design"><span>6. Activities in Activity Design</span></a></h2>
<ul>
<li><strong>Task Analysis</strong>: Outlines steps users take.</li>
<li><strong>Dialogue Modeling</strong>: Shows states and transitions in the system.</li>
</ul>
<h3 id="uml-activity-diagram-elements" tabindex="-1"><a class="header-anchor" href="#uml-activity-diagram-elements"><span>UML Activity Diagram Elements</span></a></h3>
<ul>
<li><strong>Action</strong>: Atomic task.</li>
<li><strong>Activity</strong>: Group of tasks/actions.</li>
<li><strong>Decision Node</strong>, <strong>Fork Node</strong>, <strong>Join Node</strong>, <strong>Synchronization Node</strong>.</li>
</ul>
<hr>
<h2 id="_7-task-analysis-example" tabindex="-1"><a class="header-anchor" href="#_7-task-analysis-example"><span>7. Task Analysis Example</span></a></h2>
