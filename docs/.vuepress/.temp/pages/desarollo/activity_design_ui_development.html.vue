</ul>
</li>
</ul>
<hr>
<h2 id="_8-dialogue-modeling" tabindex="-1"><a class="header-anchor" href="#_8-dialogue-modeling"><span>8. Dialogue Modeling</span></a></h2>
<ul>
<li><strong>States</strong>: System statuses.</li>
<li><strong>Transitions</strong>: Moves triggered by events or conditions.</li>
<li><strong>Initial/Final Nodes</strong>: Start/end of flow.</li>
</ul>
