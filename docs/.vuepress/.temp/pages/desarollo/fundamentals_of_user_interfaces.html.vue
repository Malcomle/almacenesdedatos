<h2 id="_1-understanding-the-user" tabindex="-1"><a class="header-anchor" href="#_1-understanding-the-user"><span>1. Understanding the User</span></a></h2>
<h3 id="motivation" tabindex="-1"><a class="header-anchor" href="#motivation"><span>Motivation</span></a></h3>
<p>Traditionally, UI design was overlooked, but research has shown that poor interfaces frustrate users. Thus, understanding human factors is crucial to improving design and usability.</p>
<h3 id="cognitive-psychology" tabindex="-1"><a class="header-anchor" href="#cognitive-psychology"><span>Cognitive Psychology</span></a></h3>
<p>Users are at the core of digital system interactions. Cognitive psychology helps us analyze how users perceive and process information. Key insights:</p>
<ul>
<li><strong>Human Processing Model</strong>:
<ol>
<li><strong>Input Devices</strong> (Vision, Hearing, Touch)</li>
<li><strong>Central Unit</strong> (Attention, Working Memory, Long-Term Memory)</li>
<li><strong>Output Devices</strong> (Limbs, Fingers, Voice)</li>
</ol>
</li>
</ul>
<h4 id="sensory-perception" tabindex="-1"><a class="header-anchor" href="#sensory-perception"><span>Sensory Perception</span></a></h4>
<ul>
<li><strong>Visual Perception</strong>: Most critical in UI design (color, brightness, size, depth).</li>
<li><strong>Auditory Perception</strong>: Important for feedback and accessibility.</li>
<li><strong>Tactile Perception</strong>: Essential for touch-based interfaces.</li>
</ul>
<h4 id="visual-perception" tabindex="-1"><a class="header-anchor" href="#visual-perception"><span>Visual Perception</span></a></h4>
<ul>
<li>Color usage is vital (avoid red-on-blue, ensure high contrast).</li>
<li>Some users have color vision deficiencies; color alone shouldn’t convey critical info.</li>
</ul>
<h4 id="attention-and-perception" tabindex="-1"><a class="header-anchor" href="#attention-and-perception"><span>Attention and Perception</span></a></h4>
<ul>
<li>Attention filters information. Overloaded interfaces can hinder focus.</li>
<li>Studies show users ignore less relevant areas (e.g., banner blindness).</li>
</ul>
<h4 id="perception-and-knowledge" tabindex="-1"><a class="header-anchor" href="#perception-and-knowledge"><span>Perception and Knowledge</span></a></h4>
<ul>
<li>Icons leverage real-world associations. They must be clear, familiar, and unambiguous.</li>
</ul>
<h4 id="affordances" tabindex="-1"><a class="header-anchor" href="#affordances"><span>Affordances</span></a></h4>
<ul>
<li>Affordance: The perceived function of an object (e.g., a button appears “pressable”).</li>
</ul>
<h4 id="text-perception" tabindex="-1"><a class="header-anchor" href="#text-perception"><span>Text Perception</span></a></h4>
<ul>
<li>Readability depends on font size (optimal 9–12pt) and line length (6–13 cm).</li>
</ul>
