<h2 id="_3-activities-for-requirements-gathering" tabindex="-1"><a class="header-anchor" href="#_3-activities-for-requirements-gathering"><span>3. Activities for Requirements Gathering</span></a></h2>
<ul>
<li><strong>Ethnographic Analysis</strong>: Observe users in their environment.</li>
<li><strong>Stakeholder Analysis</strong>: Identify all individuals involved.</li>
<li><strong>User Classification</strong>: Group users by profiles and roles.</li>
<li><strong>User Stories</strong>: Describe features from the user perspective.</li>
<li><strong>Contextual Task Analysis</strong>: Examine user tasks to identify patterns.</li>
<li><strong>Physical &amp; Conceptual Objects</strong>: Recognize tangible and intangible items.</li>
<li><strong>Technological Platform Analysis</strong>: Evaluate deployment environment.</li>
</ul>
<hr>
<h2 id="_4-user-stories" tabindex="-1"><a class="header-anchor" href="#_4-user-stories"><span>4. User Stories</span></a></h2>
<ul>
<li>Brief, user-focused descriptions of features.</li>
<li>Include title, description, acceptance criteria, and discussions.</li>
</ul>
<hr>
<h2 id="_5-contextual-task-analysis" tabindex="-1"><a class="header-anchor" href="#_5-contextual-task-analysis"><span>5. Contextual Task Analysis</span></a></h2>
<ul>
<li>Understand current tasks and workflows.</li>
<li>Uncover user goals and how they achieve them.</li>
</ul>
<hr>
<h2 id="_6-identifying-physical-conceptual-objects" tabindex="-1"><a class="header-anchor" href="#_6-identifying-physical-conceptual-objects"><span>6. Identifying Physical &amp; Conceptual Objects</span></a></h2>
<ul>
<li>Physical: Documents, tools, etc.</li>
<li>Conceptual: Events, messages, etc.</li>
</ul>
<hr>
<h2 id="_7-technological-platform-analysis" tabindex="-1"><a class="header-anchor" href="#_7-technological-platform-analysis"><span>7. Technological Platform Analysis</span></a></h2>
